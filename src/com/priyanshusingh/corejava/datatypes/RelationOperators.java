package com.priyanshusingh.corejava.datatypes;

public class RelationOperators {
    public static void main(String[] args) {
        System.out.println(1 < 2);
        System.out.println(1 == 2);
        System.out.println(1 != 4);
        System.out.println(1 > 2);
        System.out.println(1 <= 2);
    }
}
